class ContactHistoryGetSetter:

    @property
    def id_login(self):
        return self.__id_login

    @id_login.setter
    def id_login(self, id_login):
        self.__id_login = id_login

    @property
    def name(self):
        return self.__name

    @name.setter
    def name(self, name):
        self.__name = name
